void wait_for_connection(void) {
    struct pollfd fds;
    fds.fd = 0; /* this is STDIN */
    fds.events = POLLIN;
    
    picture_t *pic = picture_alloc();
    picture_raw2pic(pic, waiting);
    matrix_update(pic);
    
    for(;;) {
        int i;
        static int state = 0;
        static int ani = 0;
        
        int ret = poll(&fds, 1, 0);
        if(ret == 1)
            break;
        
        picture_scroll_part(pic, SCROLL_LEFT, 28, 6);
        switch(state) {
            case 0:
                picture_setPixel(pic, 39, 33, 1);
                break;
            case 1:
                picture_setPixel(pic, 39, 33, 1);
                break;
            case 2:
            case 5:
                for(i = 0 ; i < 6 ; i++)
                        picture_setPixel(pic, 39, 33-i, 1);
                break;
            case 3:
                picture_setPixel(pic, 39, 28, 1);
                break;
            case 4:
                picture_setPixel(pic, 39, 28, 1);
                break;
            default:
                break;
        }
        
        if(++ani == 4) {
            static int state = 0;
            ani=0;
            picture_scroll_part(pic, SCROLL_RIGHT, 35, 6);
            switch(state) {
                case 0:
                    picture_setPixel(pic, 0, 40, 1);
                    break;
                case 1:
                    picture_setPixel(pic, 0, 40, 1);
                    break;
                case 2:
                case 5:
                    for(i = 0 ; i < 6 ; i++)
                            picture_setPixel(pic, 0, 40-i, 1);
                    break;
                case 3:
                    picture_setPixel(pic, 0, 35, 1);
                    break;
                case 4:
                    picture_setPixel(pic, 0, 35, 1);
                    break;
                default:
                    break;
            }
            if(++state == 6)
                state = 0;
        }
        
        if(++state == 6)
            state = 0;
        
        usleep(50000);
        matrix_update(pic);
    }    
out:
    picture_free(pic);
}

int main(void) {
    printf("Initializing Hardware....\n");
    matrix_init();

    printf("Phasers on standby...\n");
    
    wait_for_connection();
    printf("Fire!\n");
    picture_t *pic = picture_alloc();

    while( read(0, *pic, sizeof(picture_t)) == sizeof(picture_t) ) {
        matrix_update(pic);
    }

    picture_free(pic);
    
    printf("Shutting down...\n");
    matrix_close();

    return 0;
}
